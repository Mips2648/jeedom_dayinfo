$('#typeEq').change(function () {
  var text = $("#typeEq").val();
  if (text == 'holidays' || text == 'bankdays' || text == 'global') {
    $('#cmdgeoloc').show();
  } else {
    $('#cmdgeoloc').hide();
  }
  if (text == 'calendar') {
    $('#calendar').show();
  } else {
    $('#calendar').hide();
  }
});

